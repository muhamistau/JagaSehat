package com.example.islam.jagasehat;

public class User {

    int umur;
    String jk, pendidikan, pekerjaan, email;

    public User() {

    }

    public User(int umur, String jk, String pendidikan, String pekerjaan, String email) {
        this.umur = umur;
        this.jk = jk;
        this.pendidikan = pendidikan;
        this.pekerjaan = pekerjaan;
        this.email = email;
    }
}
